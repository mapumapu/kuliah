#Luas Segitiga
print('Luas Segitiga (dalam centimeter)')
alas = int(input('Alas : '))
tinggi = int(input('Tinggi : '))
luasSegitiga = 0.5 * alas * tinggi
print('Hasil Perhitungan Luas Segitiga Adalah : '+str(luasSegitiga) + ' cm')

#Luas Lingkaran
print('Luas Lingkaran (dalam centimeter)')
jariJari = int(input('Jari -jari : '))
luasLingkaran = 3.14 * (jariJari * jariJari)
print('Hasil Perhitungan Luas Lingkaran Adalah : '+str(luasLingkaran) + ' cm')


nilai1 = 10
nilai2 = 8

penjumlahan = str(nilai1 + nilai2)
pengurangan = str(nilai1 - nilai2)
perkalian = str(nilai1 * nilai2)
pembagian = str(nilai1 / nilai2)
pemangkatan = str(nilai1 ** nilai2)
pembagianBulat = str(nilai1 // nilai2)

print('Penjumlahan ' + str(nilai1) + ' + ' + str(nilai2) + ' adalah : ' + penjumlahan)
print('Pengurangan ' + str(nilai1) + ' - ' + str(nilai2) + ' adalah : ' + pengurangan)
print('Perkalian ' + str(nilai1) + ' x ' + str(nilai2) + ' adalah : ' + perkalian)
print('Pembagian ' + str(nilai1) + ' : ' + str(nilai2) + ' adalah : ' + pembagian)
print('Pemangkatan ' + str(nilai1) + ' ** ' + str(nilai2) + ' adalah : ' + pemangkatan)
print('Pembagian Bulat ' + str(nilai1) + ' // ' + str(nilai2) + ' adalah : ' + pembagianBulat)
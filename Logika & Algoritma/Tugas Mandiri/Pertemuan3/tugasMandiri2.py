# Inisialisasi variable awal
aldi = 30
budi = aldi - 15
anto = 2 * (aldi + budi)
agung = (aldi + budi + anto) - 5

# menghitung jumlah total kelereng budi, anto, dan agung
jumlahTotal = budi + anto + agung

# print jumlah kelereng budi, anto, dan agung
